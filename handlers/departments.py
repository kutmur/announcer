def get_department_names():
    """Get list of all department names"""
    return list(DEPARTMENTS.keys())


def get_department_url(department_name):
    """Get announcement URL for a specific department"""
    return DEPARTMENTS.get(department_name, {}).get("url")


def get_department_faculty(department_name):
    """Get faculty name for a specific department"""
    return DEPARTMENTS.get(department_name, {}).get("faculty")


def get_all_departments():
    """Get the complete departments dictionary"""
    return DEPARTMENTS


def is_valid_department(department_name):
    """Check if a department name is valid"""
    return department_name in DEPARTMENTS


def get_departments_by_faculty(faculty_name):
    """Get all departments that belong to a specific faculty"""
    return [dept for dept, info in DEPARTMENTS.items() if info['faculty'] == faculty_name]


def get_all_faculties():
    """Get list of all unique faculty names"""
    return list(set(info['faculty'] for info in DEPARTMENTS.values()))
